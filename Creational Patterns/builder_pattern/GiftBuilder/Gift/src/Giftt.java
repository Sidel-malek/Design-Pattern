public class Giftt {
    private Boolean ribbonRed;
    private Boolean ribbonBlue;
    private Boolean wrapGreen;
    private Boolean wrapPurple;

    public void setRibbonRed(Boolean ribbonRed) {
        this.ribbonRed = ribbonRed;
    }

    public void setRibbonBlue(Boolean ribbonBlue) {
        this.ribbonBlue = ribbonBlue;
    }

    public void setWrapGreen(Boolean wrapGreen) {
        this.wrapGreen = wrapGreen;
    }

    public void setWrapPurple(Boolean wrapPurple) {
        this.wrapPurple = wrapPurple;
    }

    public Boolean getRibbonRed() {
        return ribbonRed;
    }

    public Boolean getRibbonBlue() {
        return ribbonBlue;
    }

    public Boolean getWrapGreen() {
        return wrapGreen;
    }

    public Boolean getWrapPurple() {
        return wrapPurple;
    }
 
}
