public class Motorcycle extends Vehicule {
    public void accelerate() {
        System.out.println("Accelerating motorcycle...");
    }

    public void decelerate() {
        System.out.println("Decelerating motorcycle...");
    }

    public void start() {
        System.out.println("Starting motorcycle...");
    }

    public void stop() {
        System.out.println("Stopping motorcycle...");
    }

    public void accelerateMotorcycle() {
        System.out.println("Accelerating motorcycle with specialized method...");
    }
}
